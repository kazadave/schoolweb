<?php

class BWGEControllerSlideshow {
  ////////////////////////////////////////////////////////////////////////////////////////
  // Events                                                                             //
  ////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////
  // Constants                                                                          //
  ////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////
  // Variables                                                                          //
  ////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////
  // Constructor & Destructor                                                           //
  ////////////////////////////////////////////////////////////////////////////////////////
  public function __construct() {
  }
  ////////////////////////////////////////////////////////////////////////////////////////
  // Public Methods                                                                     //
  ////////////////////////////////////////////////////////////////////////////////////////
  public function execute($params = array(), $from_shortcode = 0, $bwge = 0) {
    $this->display($params, $from_shortcode, $bwge);
  }

  public function display($params, $from_shortcode = 0, $bwge = 0) {
    require_once WD_BWGE_DIR . "/frontend/models/BWGEModelSlideshow.php";
    $model = new BWGEModelSlideshow();

    require_once WD_BWGE_DIR . "/frontend/views/BWGEViewSlideshow.php";
    $view = new BWGEViewSlideshow($model);
    
    $view->display($params, $from_shortcode, $bwge);
  }

  ////////////////////////////////////////////////////////////////////////////////////////
