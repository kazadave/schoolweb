<?php

class BWGEControllerImage_browser {
  ////////////////////////////////////////////////////////////////////////////////////////
  // Events                                                                             //
  ////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////
  // Constants                                                                          //
  ////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////
  // Variables                                                                          //
  ////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////
  // Constructor & Destructor                                                           //
  ////////////////////////////////////////////////////////////////////////////////////////
  public function __construct() {
  }
  ////////////////////////////////////////////////////////////////////////////////////////
  // Public Methods                                                                     //
  ////////////////////////////////////////////////////////////////////////////////////////
  public function execute($params = array(), $from_shortcode = 0, $bwge = 0) {
    $this->display($params, $from_shortcode, $bwge);
  }

  public function display($params, $from_shortcode = 0, $bwge = 0) {
    require_once WD_BWGE_DIR . "/frontend/models/BWGEModelImage_browser.php";
    $model = new BWGEModelImage_browser();

    require_once WD_BWGE_DIR . "/frontend/views/BWGEViewImage_browser.php";
    $view = new BWGEViewImage_browser($model);

    $view->display($params, $from_shortcode, $bwge);
  }
