  public function __construct() {
  }
  ////////////////////////////////////////////////////////////////////////////////////////
  // Public Methods                                                                     //
  ////////////////////////////////////////////////////////////////////////////////////////
  public function execute() {
    $task = ((isset($_POST['task'])) ? esc_html(stripslashes($_POST['task'])) : '');

    if($task != ''){
      if(!BWGELibrary::verify_nonce('uninstall_bwge')){
        die('Sorry, your nonce did not verify.');
      }
    }

    if (method_exists($this, $task)) {
      $this->$task();
    }
    else {
      $this->display();
    }
  }

  public function display() {
    require_once WD_BWGE_DIR . "/admin/models/BWGEModelUninstall_bwge.php";
    $model = new BWGEModelUninstall_bwge();

    require_once WD_BWGE_DIR . "/admin/views/BWGEViewUninstall_bwge.php";
    $view = new BWGEViewUninstall_bwge($model);
    $view->display();
  }

  public function uninstall() {
    require_once WD_BWGE_DIR . "/admin/models/BWGEModelUninstall_bwge.php";
    $model = new BWGEModelUninstall_bwge();

    require_once WD_BWGE_DIR . "/admin/views/BWGEViewUninstall_bwge.php";
    $view = new BWGEViewUninstall_bwge($model);
    $view->uninstall();
  }
